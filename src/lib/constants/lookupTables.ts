// ==========================================
// 공망 조견표 (Void/Empty Lookup)
// ==========================================
// 60갑자를 10개씩 나눠서 각 순(旬)에 빠진 2개의 지지
export function getGongMang(stemIdx: number, branchIdx: number): [number, number] {
  // 갑자순에서의 위치 계산
  // 60갑자에서의 index = (stemIdx - branchIdx + 12) % 12 ...
  // 실제로는: 순(旬)의 시작점을 찾고 빠진 지지 2개를 반환
  // 간단한 공식: 갑(stem=0)에서 시작하는 순의 경우
  // 해당 순에서 천간 0~9가 지지 시작점~시작점+9에 대응
  // 빠진 지지 = 시작점+10, 시작점+11

  // 순(旬)의 시작 지지 계산
  const startBranch = (branchIdx - stemIdx + 120) % 12;
  // 공망 = startBranch + 10, startBranch + 11
  const gm1 = (startBranch + 10) % 12;
  const gm2 = (startBranch + 11) % 12;
  return [gm1, gm2];
}
