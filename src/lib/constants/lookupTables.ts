// ==========================================
// 12운성 조견표 (Twelve Growth Stages)
// ==========================================
// table[천간 index][지지 index] = 12운성
// 양간은 순행, 음간은 역행

const GROWTH_STAGES: GrowthStage[] = ['장생', '목욕', '관대', '건록', '제왕', '쇠', '병', '사', '묘', '절', '태', '양'];

// 양간 장생 위치 (지지 index)
// 갑(양목): 해(11), 병(양화): 인(2), 무(양토): 인(2), 경(양금): 사(5), 임(양수): 신(8)
const YANG_STEM_BIRTH: number[] = [11, -1, 2, -1, 2, -1, 5, -1, 8, -1];

// 음간 장생 위치 (지지 index) - 역행
// 을(음목): 오(6), 정(음화): 유(9), 기(음토): 유(9), 신(음금): 자(0), 계(음수): 묘(3)
const YIN_STEM_BIRTH: number[] = [-1, 6, -1, 9, -1, 9, -1, 0, -1, 3];

export function getGrowthStage(stemIdx: number, branchIdx: number): GrowthStage {
  const isYang = stemIdx % 2 === 0;

  if (isYang) {
    const birthBranch = YANG_STEM_BIRTH[stemIdx];
    const offset = (branchIdx - birthBranch + 12) % 12;
    return GROWTH_STAGES[offset];
  } else {
    const birthBranch = YIN_STEM_BIRTH[stemIdx];
    // 음간은 역행
    const offset = (birthBranch - branchIdx + 12) % 12;
    return GROWTH_STAGES[offset];
  }
}

// ==========================================
// 월주 천간 조견표
// ==========================================
// 년간에 따른 1월(인월)의 천간
// 갑기년 -> 병인월, 을경년 -> 무인월, 병신년 -> 경인월, 정임년 -> 임인월, 무계년 -> 갑인월
export const MONTH_STEM_BASE: number[] = [2, 4, 6, 8, 0]; // 갑기:병, 을경:무, 병신:경, 정임:임, 무계:갑

export function getMonthStem(yearStemIdx: number, monthBranchIdx: number): number {
  // monthBranch: 인(2)=1월, 묘(3)=2월, ..., 축(1)=12월
  const group = yearStemIdx % 5;
  const base = MONTH_STEM_BASE[group];
  // 인월(2)부터 시작, 인월까지의 offset
  const monthOffset = (monthBranchIdx - 2 + 12) % 12;
  return (base + monthOffset) % 10;
}

// ==========================================
// 시주 천간 조견표
// ==========================================
// 일간에 따른 자시의 천간
// 갑기일 -> 갑자시, 을경일 -> 병자시, 병신일 -> 무자시, 정임일 -> 경자시, 무계일 -> 임자시
export const HOUR_STEM_BASE: number[] = [0, 2, 4, 6, 8]; // 갑기:갑, 을경:병, 병신:무, 정임:경, 무계:임

export function getHourStem(dayStemIdx: number, hourBranchIdx: number): number {
  const group = dayStemIdx % 5;
  const base = HOUR_STEM_BASE[group];
  return (base + hourBranchIdx) % 10;
}

